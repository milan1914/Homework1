package oop;

public class Pet {
    private boolean hasTail;
    private String name;
    private int age;
    private String breed0;

    public boolean isHasTail() {
        return hasTail;
    }

    public void setHasTail(boolean hasTail) {
        this.hasTail = hasTail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getBreed0() {
        return breed0;
    }

    public void setBreed0(String breed0) {
        this.breed0 = breed0;
    }
}


